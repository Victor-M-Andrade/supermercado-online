    private void login(){
        //Parte da verificação do login
        if (new String(txtSenha.getPassword()).equals(key)
                    && user.equals(txtUsuario.getText())) {
            JanelaInicial janelaInicial = new JanelaInicial();
            janelaInicial.setVisible(true);
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Senha ou usuário incorreto", "ERROR", JOptionPane.ERROR_MESSAGE);
        
        }
        
    }
