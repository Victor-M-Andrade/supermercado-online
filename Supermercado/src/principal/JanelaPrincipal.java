    private void selMostrarSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selMostrarSenhaActionPerformed
        //troca do icone do mostrar ou não a senha
        if (selMostrarSenha.isSelected()){
            txtSenha.setEchoChar('\u0000');
            jLabel5.setIcon(new javax.swing.ImageIcon(getClass().getResource("/principal/ocultarSenha.png")));
            selMostrarSenha.setText("Ocultar senha");
        } else {
            txtSenha.setEchoChar(senha);
            jLabel5.setIcon(new javax.swing.ImageIcon(getClass().getResource("/principal/exibirSenha.png")));
            selMostrarSenha.setText("Mostrar senha");

        }
    }//GEN-LAST:event_selMostrarSenhaActionPerformed

